#include "libft.h"

static int	ft_is_in_set(char c, const char *set)
{
	while (*set)
	{
		if (*set == c)
			return (1);
		set++;
	}
	return (0);
}

char	*ft_strtrim(char const *s1, char const *set)
{
	size_t	start;
	size_t	end;
	size_t	new_len;
	char	*trimmed_str;

	if (!s1 || !set)
		return (NULL);
	start = 0;
	end = ft_strlen(s1);
	while (s1[start] && ft_is_in_set(s1[start], set))
		start++;
	while (end > start && ft_is_in_set(s1[end - 1], set))
		end--;
	new_len = end - start;
	trimmed_str = (char *)ft_calloc(new_len + 1, sizeof(char));
	if (!trimmed_str)
		return (NULL);
	ft_memcpy(trimmed_str, s1 + start, new_len);
	return (trimmed_str);
}
